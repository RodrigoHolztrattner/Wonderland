template <typename T>
class IndirectionPointer
{
public:

	// Set the internal pointer
	void Set(T* _object)
	{
		m_Ptr = _object;
	}

	// Get
	template <typename CastType>
	CastType* Get()
	{
		return (CastType*)m_Ptr;
	}
	T* Get()
	{
		return m_Ptr;
	}

private:

	T* m_Ptr;
};
